## Set 

# it is denoted by { } 
# Unorder : it does not maintain values in insertion order.
# Mutuable : after creation you can be change allowed (add,remove) but (index is not allowed)
# Duplicate : does not allowed duplicate values



##Syntax  

set = {10, 20, 30, 40}
print(set)
set.add(70)
set.remove(20)
print(set)

##Not allowed

print(set.index(30))


